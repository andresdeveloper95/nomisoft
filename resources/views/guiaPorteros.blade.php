   <p> Esta interfaz proporciona las opciones para Administrar Porteros. </p>
   
   <h3>3.1.2.1 Crear Portero </h3>

    <p>En la parte superior derecha se encuentra el botón de Crear Portero el cual desplegará un interfaz para
     agregar todo la información necesaria del portero.
   </p>
     <center> <img src="./img/OpcrearPortero.jpg"/ style=" width: 600px; height: 350px;"> </center>

    <p>Cuando todos los campos  estén diligenciados correctamente (documento,  nombres, apellidos, dirección, teléfono, correo). 
    Deberá hacer click en el botón  Crear Portero, posteriormente se desplegará un mensaje confirmando que el portero ha sido 
    creado exitosamente.
    </p>
    <center> <img src="./img/crearPortero.jpg"/ style=" width: 400px; height: 300px;"> </center>

    <h3> 3.1.2.2 Actualizar Portero</h3>
    <p> En la parte derecha aparecerá un icono amarillo con el cual se podrá actualizar la información necesaria de los Porteros.
      Deberá darle Click y se desplegará una interfaz en la cual podrá modificar la información deseada. </p>

      <center> <img src="./img/OpactuPortero.jpg"/ style=" width: 600px; height: 350px;"> </center>

      <p> Cuando los campos estén editados correctamente (nombres, apellidos, dirección, teléfono,
       correo). Deberá dar Click en el botón Actualizar Portero. Se desplegará un mensaje de 
       confirmación preguntando si esta seguro de realizar la acción y luego otro mensaje confirmando 
       la actualización de la información del portero.</p>
     <center> <img src="./img/aPortero.jpg"/ style=" width: 400px; height: 300px;"> </center>

     <h3>3.1.2.3 Eliminar Portero </h3>

    <p>En la parte derecha aparecerá un icono rojo con el cual se podrá eliminar  la información de 
    los Porteros. Deberá darle Click y se desplegará un mensaje de confirmación preguntando si está 
    seguro de realizar la acción y finalmente otro mensaje informando que el portero ha sido eliminado 
    exitosamente.  </p>

