    <h1>Administrar Horarios</h1>

    <p> Esta interfaz proporciona las opciones para Administrar horarios.</p>

    <center> <img src="./img/AdminHorarios.jpg"/ style=" width: 600px; height: 250px;"> </center>

    <h3>3.1.3.1 Crear Horario</h3>
    
    <center> <img src="./img/opcrearHorario.jpg"/ style=" width: 600px; height: 250px;"> </center>

    <p>En la parte superior se encuentra el botón de Crear Horario el cual desplegará una
     interfaz para agregar todo la información necesaria para crear el horario.</p>

    <center> <img src="./img/crearHorario.jpg"/ style=" width: 400px; height: 500px;"> </center>

    <p> Esta interfaz proporciona la opcion de crear el horario de los porteros para un mes completo. </p>
    <p> Para crear correctamente el horario se debe selecionar el nombre del portero, luego debe selecionar 
    la sede y el mes. Posteriormente se debe seleccionar en cada dia el turno que desea asignarle 
    al portero teniendo en cuenta que no debe sobrepasar el limite de 48 horas de trabajo continuo. 
    Deben estar llenos todos los campos de la interfaz y por ultimo dar click en el boton de Crear Horario.</p>

    <h3>3.1.3.2 Actualizar Horario</h3>

    <center> <img src="./img/opcactuHorario2.jpg"/ style=" width: 600px; height: 200px;"> </center>

    <p> En la parte derecha aparecerá un icono amarillo con el cual podrá actualizar la información
     necesaria de los horarios. Deberá darle Click y se desplegará una interfaz en la cual podrá 
     modificar la información deseada. </p> 

    <center> <img src="./img/aHorario.jpg"/ style=" width: 400px; height: 500px;"> </center>

    <p> Cuando los campos estén editados correctamente (portero, sede, y turnos diarios). Deberá dar Click
    en el botón Actualizar horario. Se desplegará un mensaje de confirmación preguntando si esta seguro que 
    desea realizar la acción y luego otro mensaje confirmando la actualización de la información del horario.</p>

    <h3>3.1.3.3 Eliminar Horario</h3>

     <p>En la parte derecha aparecerá un icono rojo con el cual se podrá eliminar la información de
     los horarios. Deberá darle Click y se desplegará un mensaje de confirmación preguntando si está 
     seguro de realizar la acción y finalmente otro mensaje informando que el horario ha sido eliminado
     exitosamente. </p>

